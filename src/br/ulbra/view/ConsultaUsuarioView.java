    /**
     * Método para popular o JComboBox usando sua Factory
     */
    private void carregarComboUsuarios() {
        String sql = "SELECT nome FROM Usuarios ORDER BY nome";

        // Usamos a sua ConnectionFactory aqui
        try (Connection con = ConnectionFactory.getConnection();
                PreparedStatement pst = con.prepareStatement(sql);
                ResultSet rs = pst.executeQuery()) {

            cbUsuarios.removeAllItems();
            cbUsuarios.addItem("Todos");

            while (rs.next()) {
                cbUsuarios.addItem(rs.getString("nome"));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar combo: " + e.getMessage());
        }
    }

