    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       // Captura o texto (selecionado ou digitado se for editable)
        String busca = cbUsuarios.getEditor().getItem().toString().trim();
        
        DefaultTableModel modelo = (DefaultTableModel) tbUsuarios.getModel();
        modelo.setNumRows(0); 

        String sql;
        if (busca.equals("Todos") || busca.isEmpty()) {
            sql = "SELECT * FROM Usuarios";
        } else {
            // SQL seguro com parâmetro
            sql = "SELECT * FROM Usuarios WHERE nome LIKE ?";
        }

        // Novamente, chamando sua Factory
        try (Connection con = ConnectionFactory.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {
            
            if (!busca.equals("Todos") && !busca.isEmpty()) {
                pst.setString(1, "%" + busca + "%");
            }

            ResultSet rs = pst.executeQuery();
            
            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id_usuario"),
                    rs.getString("nome"),
                    rs.getString("email")
                });
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro na consulta: " + e.getMessage());
        }
    
    }//GEN-LAST:event_btnBuscarActionPerformed

