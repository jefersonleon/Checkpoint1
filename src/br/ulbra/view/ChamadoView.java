    private void configurarUsuarioLogado() {
        Usuario logado = Sessao.getUsuario();

        if (logado == null) {
            return; // Segurança caso não haja sessão
        }
        // Percorre o combo
        for (int i = 0; i < cbSolicitante.getItemCount(); i++) {
            // O truque está aqui: Pegamos como Object primeiro
            Object item = cbSolicitante.getItemAt(i);

            // Verificamos se o item é do tipo Usuario antes de converter
            if (item instanceof Usuario) {
                Usuario u = (Usuario) item;

                if (u.getId() == logado.getId()) {
                    cbSolicitante.setSelectedIndex(i);
                    break;
                }
            }

            // Opcional: Trava o combo para o usuário não mudar o solicitante
            cbSolicitante.setEnabled(false);
        }
        // Opcional: desabilita o combo para o usuário não abrir chamado no nome de outro
        cbSolicitante.setEnabled(false);
    }
    ChamadoController controller = new ChamadoController();

    private void carregarCombos() {
        // Carregando Usuários
        List<Usuario> usuarios = new UsuarioDAOImpl().listarTodos();
        DefaultComboBoxModel modelUser = new DefaultComboBoxModel();
        for (Usuario u : usuarios) {
            modelUser.addElement(u); // Adiciona o objeto inteiro
        }
        cbSolicitante.setModel(modelUser);

        // Carregando Equipamentos
        List<Equipamento> equipamentos = new EquipamentoDAOImpl().listarTodos();
        DefaultComboBoxModel modelEqui = new DefaultComboBoxModel();
        for (Equipamento e : equipamentos) {
            modelEqui.addElement(e); // Adiciona o objeto inteiro
        }
        cbEquipamento.setModel(modelEqui);
    }

    public void listarTabela() {
        DefaultTableModel model = (DefaultTableModel) tbChamado.getModel();
        model.setRowCount(0);

        // Pegamos o usuário da sessão
        Usuario logado = Sessao.getUsuario();
        JOptionPane.showMessageDialog(this, logado.getId());
        // Chamamos o novo método do DAO passando o ID do logado
        // Nota: Certifique-se que o seu Controller tenha o método correspondente
        List<Chamado> chamados = controller.listarPorUsuario(logado.getId());

        for (Chamado c : chamados) {
            model.addRow(new Object[]{
                c.getId(),
                c.getUsuario().getNome(),
                c.getEquipamento().getTag_patrimonio(),
                c.getEquipamento().getSala(),
                c.getProblemaRelatado(),
                c.getDiagnosticoTecnico(),
                c.getPrioridade(),
                c.getStatus(),
                c.getDataAbertura()
            });
        }
    }

