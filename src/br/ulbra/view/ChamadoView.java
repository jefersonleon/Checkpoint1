    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            // 1. VALIDAÇÃO DE SELEÇÃO (Combos)
            if (cbSolicitante.getSelectedItem() == null || cbEquipamento.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(null, "Por favor, selecione um solicitante e um equipamento!");
                return;
            }

            // 2. CAPTURA DOS OBJETOS (Recuperando os IDs reais do banco)
            Object objUser = cbSolicitante.getSelectedItem();
            Object objEqui = cbEquipamento.getSelectedItem();

            // Verificação de segurança (Garante que o cast não vai falhar)
            if (!(objUser instanceof Usuario) || !(objEqui instanceof Equipamento)) {
                JOptionPane.showMessageDialog(null, "Erro: Os itens selecionados não são objetos válidos.");
                return;
            }

            Usuario user = (Usuario) objUser;
            Equipamento equip = (Equipamento) objEqui;

            // 3. CAPTURA DOS CAMPOS DE TEXTO E COMPONENTES
            int idUser = user.getId(); // Use getIdUsuario() se for o nome no seu Model
            int idEqui = equip.getId_equipamento();

            String problema = txtProblema.getText().trim();
            String diagnostico = txtDiagnostico.getText().trim();
            String prioridade = cbPrioridade.getSelectedItem().toString();
            String status = chStatus.isSelected() ? "Ativo" : "Não Ativo";
//            String data = txtData.getText().trim();
// PEGA A DATA DO COMPONENTE E FORMATA COMO STRING
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");

            // Se estiver usando o JDateChooser (biblioteca externa):
            // String dataString = sdf.format(jdDataAbertura.getDate());
            // Se estiver usando um JFormattedTextField com máscara:
            String dataString = txtData.getText();

            String mensagem = controller.cadastrar(
                    idUser,
                    idEqui,
                    problema,
                    diagnostico,
                    prioridade,
                    status,
                    dataString
            );

            // 6. FEEDBACK E ATUALIZAÇÃO DA TELA
            JOptionPane.showMessageDialog(null, mensagem);
            limpar(); // Limpa os campos após salvar
            listarTabela(); // Recarrega a JTable com o novo chamado

        } catch (IllegalArgumentException e) {
            // Erro específico de formato de data no Timestamp.valueOf
            JOptionPane.showMessageDialog(null, "Erro no formato da data! Use: yyyy-mm-dd hh:mm:ss");
        } catch (Exception e) {
            // Qualquer outro erro (Banco, NullPointer, etc)
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao salvar: " + e.getMessage());
        }


    }//GEN-LAST:event_btnSalvarActionPerformed

